		<h1>订购流程</h1>
		
					<h3>挑选商品</h3>
<h3>放入购物车</h3>
<h3>选择特惠商品</h3>
<p>选择特惠品，需要您点击特惠品并放入购物车，确认特惠品是否出现在购物车的商品名称中。选择成功后,特惠品和您订购的商品是在一起的。如订单中没有特惠品，则系统默认为放弃特惠品。</p>
<h3>进入结算中心</h3>
<h3>用户登录、注册</h3>
<p>如果您是老顾客,请直接输入用户名和密码登录；如果您是新顾客,请输入您常用的电子邮箱作为用户名,并设定密码,点击&ldquo;完成&rdquo;。</p>
<h3>填写收货人信息</h3>
<h3>选择送货方式</h3>
<p>根据您所在的地区填写送货方式。您所在的地区是中国大陆，并在送货上门的范围，您可以选择快递送货上门，请参见&ldquo;<a href="/article-41.html" target="_blank">款到区域及配送时间</a>&rdquo;。如果不在送货上门的范围，请选择款到快递。</p>
<h3>选择支付方式</h3>
<p>我们提供的支付方式有：货到付款，支付宝支付，网上银行支付，快钱，财付通和银行汇款。</p>
<h3>订单确认</h3>
<p>完成上述所有流程后您可以点击&ldquo;提交订单&rdquo;，订单提交以后页面会提示订单号。提交订单表示您已经阅读并接受了TS官方网站商城的&ldquo;使用条件&rdquo;。</p>
<h3>验货签收</h3>
<p><strong>签收时请注意：</strong></p>
<p>货到付款订单&mdash;&mdash;</p>
<ul>
    <p>快递送货上门时，请务必当面对照发货单核对商品，确认颜色、尺码、数量、质量等无问题后再签收。</p>
    <p>客服热线：400-666-7900</p>
</ul>
<p>款到发货订单&mdash;&mdash;</p>
<ul>
    <p>款到发货订单，请您在签收之后核对商品，有任何问题可致电客服热线：400-666-7900 办理退换货。</p>
</ul>
<h3>温馨提示</h3>
<p>发票开具：</p>
<ul>
    <li>在提交订单页面时可以修改发票开具，您可以填写抬头，TS官方网站商城发票内容只可以开具&ldquo;服装&rdquo;。</li>
</ul>
