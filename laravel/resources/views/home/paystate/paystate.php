		<h1>支付方式说明</h1>
		
					<p>我们为您提供多种结算方式，您可以根据自身情况选择方便的方式支付货款。</p>
<h2>货到付款支付</h2>
<p>TS官方网站商城配送员送货上门，客户收单验货后，直接将货款交给配送员的一种结算方式。（注：目前暂未开通刷卡服务，货到后需要现金支付）<br />
<a target="_blank" href="http://www.onlyts.cn/article-42.html">点此查看</a>货到付款的支持范围。</p>
<h3>商品验收：</h3>
<ol>
    <li>如您验收商品时发现商品包装破损、短缺或错误、质量问题等，请您现场向配送人员指出后拒收整个包裹，或拨打4006667900联系客服。</li>
    <li>若已在发货单上签字确认，事后发现缺货情况，TS官方网站商城将不接受退换货。</li>
</ol>
<h2>在线支付</h2>
<ol>
    <li>TS官方网站商城为您提供工行、招行和支付宝等在线支付方式，几乎涵盖所有大中型银行发行的银行卡，覆盖率达98％。 选择在线支付，您的银行卡需要开通相应的在线支付功能。</li>
    <li>因各地银行政策不同，建议您在网上支付前拨打所在地银行电话，咨询该行可供网上支付的银行卡种类及开通手续。</li>
    <li>目前各银行对于网上支付均有一定金额的限制，由于各银行政策不同，建议您在申请网上支付功能时向银行咨询相关事宜。</li>
    <li>网上支付均是支付成功即到账。若由于网络故障导致您已支付成功的订单未改变订单状态，请您联系我们的客服人员为您解决。</li>
</ol>
<p>温馨提示：在线支付付款等待期限为24小时。请您在订购成功后24小时内完成支付，否则我们将不会保留您的订单。</p>
<p>&nbsp;</p>
<h2>各支付方式的付款等待期限</h2>
<p>选择网银在线支付、银行汇款、邮局汇款时，如果在以下规定时间内未收到您的款项，系统将自动取消订单。</p>
<ul>
    <li>网上支付： 等待1天</li>
    <li>银行汇款： 等待5天</li>
    <li>邮局汇款： 等待10天</li>
</ul>
